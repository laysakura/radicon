/*
    「送信モード」での各ファンクションキーに割当てるリモコンフォーマット
    リモコンコードを変更する場合は、以下の手順で行えます
        ■リモコンフォーマットを変更する場合
            ① ir_format_tbl の上から順にファンクションキー 1～4となります
            ② ファンクションキーに対応した値に変更してください
               (例)ファンクションキー１のフォーマットを変更する場合
                   ir_format_tbl の一番上の値が「FORM_NEC」(NECフォーマット)になっています
                   ここを、「FORM_KDN」に書き換えると家電協フォーマットになります

        ■リモコンコードを変更する場合
            ① 各ファンクションキーに対応したリモコンコード群は、
               ir_func1_code_tbl～ir_func4_code_tblになります
            ② ir_func _code_tbl内のデータは、上から順に動作キー1～12となります
            ② 変更したいファンクションキーに対応した ir_func _code_tbl 内で
               各動作キーに対応した値を書き換えてください
               ※4～6byteのデータ通信ですが、上手く動作しないため、
                 8byte目までに 0x00を入れてあります
*/

